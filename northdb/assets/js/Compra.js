//Viaje a atacama

const boton1 = document.getElementById('viaje');

boton1.addEventListener('click', function() {
  Swal.fire({
    title: 'Redireccionando',
    text: 'Por favor, espera...',
    icon: 'info',
    timer: 2000, // Duración del mensaje en milisegundos (2 segundos en este caso)
    showConfirmButton: false,
    willOpen: () => {
      // Iniciar la redirección después del tiempo especificado
      setTimeout(() => {
        window.location.href = 'https://buy.stripe.com/test_00gaIc64U3e20N2eUU'; // URL de redirección
      }, 2000);
    }
  });
});


